import mongoose from "mongoose";

// Define the schema
const itemSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

const ItemModel = mongoose.models.Item || mongoose.model("Item", itemSchema);

export default ItemModel;